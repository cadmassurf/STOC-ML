      DOUBLE PRECISION FUNCTION AKNDSN2(CL,T)
C
C ... クヌードセンの式により密度を計算する
C        CL : 塩素量濃度(‰) , T : 温度(°C)
C
      IMPLICIT NONE
C
      REAL(8),INTENT(IN)::CL,T
      REAL(8),PARAMETER::AT0=4.7869D0, AT1=9.8185D-2, AT2=1.0843D-3
      REAL(8),PARAMETER::BT0=1.8030D1, BT1=8.1640D-1, BT2=1.6670D-2
      REAL(8),PARAMETER::S00=0.093D0, S01=0.8149D0, S02=4.820D-4,
     $                   S03=0.680D-5
      REAL(8),PARAMETER::SS0=3.98D0, SS1=503.570D0, SS2=283.0D0,
     $                   SS3=67.26D0
      REAL(8),PARAMETER::ST0=0.1324D0, ST1=1.0D0
      REAL(8)::AT,BT,S0,SS,ST
C
C     DATA AT0,AT1,AT2     / 4.7869D0 , 9.8185D-2 , 1.0843D-3 /
C     DATA BT0,BT1,BT2     / 1.8030D1 , 8.1640D-1 , 1.6670D-2 /
C     DATA S00,S01,S02,S03 / 0.093D0 , 0.8149D0 , 4.820D-4 , 0.680D-5 /
C     DATA SS0,SS1,SS2,SS3 / 3.98D0 , 503.570D0 , 283.0D0 , 67.26D0 /
C     DATA ST0,ST1         / 0.1324D0 , 1.0D0 /
C
      AT = T*(AT0-AT1*T+AT2*T**2)*1.0D-3
      BT = T*(BT0-BT1*T+BT2*T**2)*1.0D-6
      S0 = -S00+S01*CL-S02*CL**2+S03*CL**3
      SS = -(T-SS0)**2/SS1*(T+SS2)/(T+SS3)
      ST = SS+(S0+ST0)*(ST1-AT+BT*(S0-ST0))
      AKNDSN2 = ST+1.0D3
C
      RETURN
      END
